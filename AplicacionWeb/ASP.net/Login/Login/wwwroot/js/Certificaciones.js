function addCertificacion2() {
    var elem = document.getElementById('remover');
    elem.parentNode.removeChild(elem);

    var x = document.getElementById("addCerti2");
    var y = document.getElementById("button2");
    if (x.style.display === "none") {
        x.style.display = "";
        y.style.display = "";
    }

}

